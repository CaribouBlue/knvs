import express from 'express';


////////////////////////////////////////////////////////////////////////////
// Server Setup                                                                       
////////////////////////////////////////////////////////////////////////////
const app = express();
const port = 1337;
app.listen(port, () => console.log(`Listening on port ${port}`))


////////////////////////////////////////////////////////////////////////////
// Middleware                                                                       
////////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////////
// App Routes                                                                       
////////////////////////////////////////////////////////////////////////////
app.get('/', (req, res) => {
  res.send('Hello');
});


////////////////////////////////////////////////////////////////////////////
// Api Routes                                                                       
////////////////////////////////////////////////////////////////////////////
const api = express.Router();
app.use('/api', api);

api.get('/', (req, res) => {
  res.send('Hello API');
});
